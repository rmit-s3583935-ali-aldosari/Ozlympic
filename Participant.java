
package Ozlympic;


public abstract class Participant {
  protected String name;
  protected String state;
  protected int age;
  protected int id;
  
  
public String getName() {
	return name;
}
public String getState() {
	return state;
}
public int getAge() {
	return age;
}
public int getId() {
	return id;
}

  

    
    
}
